public class Tool {
    private String name;
    private char ID;
    private int useCost;
    private float expGain;

    public Tool(String name, char ID, int useCost, float expGain) {
        this.name = name;
        this.ID = ID;
        this.useCost = useCost;
        this.expGain = expGain;
    }
}


